#Crea un programa que solicite dos valores numéricos, un operador y una potencia, 
# y realice la exponenciación entre estos dos valores.

base = int(input('Escirba un número que sera la base: '))
exponente = int(input('Esciba un número que sera el exponente: '))

print(f'La potencia de {base} elevada a {exponente} es {base ** exponente}.')