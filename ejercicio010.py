#Crea un programa que solicite dos valores numéricos al usuario y 
# luego imprima la resta del primero menos el segundo valor.

numeroUno = int(input('Escriba el primer número: '))
numeroDos = int(input('Escriba el segundo número: '))

print(f'La resta de {numeroUno} - {numeroDos} es {numeroUno - numeroDos}.')