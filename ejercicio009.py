#Crea un programa que solicite tres valores numéricos al usuario y 
# luego imprima la suma de los tres valores.

numeroUno = int(input('Escriba el primer número: '))
numeroDos = int(input('Escriba el segundo número:'))
numeroTres = int(input('Escriba el tercer númro: '))

print(f'La suma de {numeroUno} + {numeroDos} + {numeroTres} es {numeroUno + numeroDos + numeroTres }.')